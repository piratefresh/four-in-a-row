import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const sourcePath = path.join(repoRoot, "public", "CSW24.txt");
const outputPath = path.join(repoRoot, "convex", "csw24.generated.ts");

const rawWords = fs
  .readFileSync(sourcePath, "utf8")
  .split(/\r?\n/)
  .map((word) => word.trim().toUpperCase())
  .filter((word) => /^[A-Z]{2,7}$/.test(word));

const uniqueWords = [...new Set(rawWords)].sort();
const validWords = Object.fromEntries(uniqueWords.map((word) => [word, true]));

const wordsBySignature = new Map();
for (const word of uniqueWords) {
  const signature = [...word].sort().join("");
  const existing = wordsBySignature.get(signature);
  if (existing) {
    existing.push(word);
  } else {
    wordsBySignature.set(signature, [word]);
  }
}

const sortedSignatures = [...wordsBySignature.entries()].sort(([left], [right]) =>
  left.localeCompare(right),
);

const fileContents = `/* eslint-disable */
// @ts-nocheck
// This file is generated by scripts/generate-csw24-index.mjs from public/CSW24.txt.

export const CSW24_STATS = ${JSON.stringify(
  {
    totalWords: uniqueWords.length,
    signatureCount: sortedSignatures.length,
    minLength: 2,
    maxLength: 7,
  },
  null,
  2,
)} as const;

export const VALID_WORDS = ${JSON.stringify(validWords)} as const;

export const WORDS_BY_SIGNATURE = ${JSON.stringify(
  Object.fromEntries(sortedSignatures),
)} as const;
`;

fs.writeFileSync(outputPath, fileContents);
console.log(
  `Generated ${path.relative(repoRoot, outputPath)} with ${uniqueWords.length} words and ${sortedSignatures.length} signatures.`,
);
